//
// 00 - Round to nearest or even
// 01 - Round down
// 10 - Round up
// 11 - Chop
	.type	__xgetRD,@function
	.text
	.globl	__xgetRD
	.align	4
__xgetRD:
	lghi	%r2,0
	efpc	%r2,0
	nill	%r2,3
	br	%r14
	.align	4
	.size	__xgetRD,.-__xgetRD
